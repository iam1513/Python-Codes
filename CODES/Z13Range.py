# RANGE : (starting position, last position) --> Last position number won't be printed
print("\n 1. ")
for i in range(1,5):
    print(i)  # 1,2,3,4 will be printed

# RANGE : (starting position, last position,times we want to increment)
print("\n 2. ")
for i in range(0,10,2):
    print(i)  # 0,2,4,6,8 will be printed