# TUPLE : > Immutable and ordered > Represented by () > Store multiple items in single variable 
#         > built-in Datatype > indexing starts from 0 > can have same values

fruits = ("cherry","mango","guava") # Can't append , Can't Pop (Can't add or remove)
print(fruits)

fruits = ("cherry","mango","guava","mango","cherry") # Can have duplicates
print(fruits)