x = int(input("Enter x : "))
y = int(input("Enter y : "))

# Addition
sum = x + y
print("\nSum is :",sum)

# Substraction
sub = x - y
print("\nDifference is :",sub)

# Multiplication
mul = x * y
print("\nProduct is :",mul)

# Division
div = x / y
print("\nDivision is :",div)

# Modulo
mod = x % y
print("\nRemainder is :", mod)

# Floor Division
fl = x // y
print("\nFloor Division is :",fl)

# Exponent 
ex = x**y
print("\nAns of x^y is : ", ex)